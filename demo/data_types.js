// undefined
var undefinedData = undefined;
console.log(typeof undefinedData);

// null
var nullData = null;
console.log(typeof nullData);

// boolean
var boolData = false; // could have been false
console.log(typeof boolData);

// number
var numData = 2;
console.log(typeof numData);

// string
var strData = 'this is a string';
console.log(typeof strData);

// object
var objData = {
  name: 'Andy',
  hobby: 'tennis'
};
console.log(typeof objData);

// array
var arrData = [1, 2, 3, 4, 5];
console.log(typeof arrData);
